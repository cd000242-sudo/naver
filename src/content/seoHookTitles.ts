/**
 * [v2.12.0] SEO Hook Title Engine — "검색은 잡고, 클릭은 못 참게" 제목 블록.
 *
 * 홈판(neoHookTitles)과 목적이 다르다:
 *   - 홈판: 검색 무시, 엄지 멈춤만. 미세장면/날것질문 자유.
 *   - SEO : 검색 매칭(키워드 앞배치)을 살린 채로 클릭까지 뺏어야 함.
 *
 * 설계 전제(사용자 지시):
 *   "공장처럼 찍어내는 자동화가 아니다. 사람보다 더 사람처럼 쓴다.
 *    제목만 봐도 무슨 글인지 알면서(주제 명확), 결론은 몰라서 궁금하고(정보 갭),
 *    상황·감정·공감을 건드려(사람 냄새) 나도 모르게 클릭하게."
 *
 * Dependencies: shares the AI-cliche blacklist with neoHookTitles to keep
 * "사람 냄새" enforcement consistent across SEO/homefeed.
 */

import { NEO_CLICHE_BLACKLIST } from './neoHookTitles.js';
import { resolveCTRCategory, type CTRCategory } from './ctrCombat.js';

// ═══════════════════════════════════════════════════════════════════
// 카테고리별 "상황 시드" — 독자가 처한 구체 장면을 LLM이 떠올리게 하는 트리거.
//   상황을 건드려야 공감이 생기고, 공감이 생겨야 "내 얘기네" 하고 멈춘다.
// ═══════════════════════════════════════════════════════════════════

const CATEGORY_SITUATION_SEED: Record<CTRCategory, string> = {
  food: '냉장고 남은 재료, 손님 갑자기 온 날, 다이어트 중 야식 충동, 자취 첫 요리 실패',
  parenting: '등원 전쟁, 밤중 수유, 떼쓰는 마트, 또래보다 느린 것 같아 불안',
  beauty: '화장 들뜬 오후, 갑자기 잡힌 약속, 환절기 뒤집어진 피부, 거울 보다 한숨',
  health: '아침에 안 떠지는 눈, 계단 오르다 숨참, 건강검진 빨간불, 작년보다 안 빠지는 살',
  travel: '연차 붙는 주말, 짐 싸다 빠뜨린 것, 도착하니 다른 사진, 가성비 숙소 고르다 멘붕',
  tech: '쓰던 거 갑자기 느려짐, 새로 샀는데 호환 안 됨, 설명서 봐도 모름, 비싼 거 샀다 후회',
  lifestyle: '대청소하다 발견, 좁은 자취방 수납, 장마철 눅눅함, 이사하며 버린 것',
  entertainment: '주말 정주행, 다들 본다는데 나만 안 봄, 결말 보고 멍, 기대했다 실망',
  finance: '월급날 통장 스침, 안 쓴 줄 알았는데 빠진 돈, 적금 깰까 고민, 남들 다 한다는 재테크',
  general: '문득 검색하다, 다들 겪는데 말 안 하는, 진작 알았으면, 사소한데 매번 거슬리는',
};

/**
 * SEO 모드 시스템 프롬프트에 주입하는 제목 후킹 블록.
 * buildFullPrompt(mode==='seo')에서 호출된다.
 */
export function buildSeoHookPromptBlock(categoryHint?: string, primaryKeyword?: string): string {
  const cat = resolveCTRCategory(categoryHint);
  const kw = (primaryKeyword || '').trim() || '{메인키워드}';
  const situations = CATEGORY_SITUATION_SEED[cat];
  // 블랙리스트는 앞쪽 대표 16개만 노출 (토큰 절약, 나머지는 평가 게이트가 잡음)
  const blacklistSample = NEO_CLICHE_BLACKLIST.slice(0, 16).join(', ');

  return `
════════════════════════════════════════════════════════════
[SEO HOOK TITLE] — "검색은 잡고, 클릭은 못 참게" 제목 의무 규칙
════════════════════════════════════════════════════════════

이 툴은 공장처럼 찍어내는 자동화가 아니다. 사람보다 더 사람처럼 쓴다.
제목 한 줄이 그 증거다. 아래 4가지를 '동시에' 만족하지 못하면 실패 → 다시 쓴다.

① 주제 명확 (검색 생존)
   - 메인키워드 "${kw}"를 제목 앞 1/3 안에 '그대로' 넣는다. 뒤로 밀거나 빼면 검색 사망.
   - 제목만 봐도 무슨 글인지 1초 안에 안다. 낚시·과장 금지.

② 정보 갭 (클릭 강제)
   - 핵심 결론·숫자·이유는 제목에서 '감춘다'. 읽는 순간 "그래서 어떻게 됐는데?"가 떠올라야 한다.
   - 답을 제목에 다 적으면 클릭할 이유가 사라진다. 결론은 본문에서 푼다.

③ 상황 + 감정 + 공감 (사람 냄새 — 가장 중요)
   - 독자가 처한 '구체적 장면'을 건드린다. 이 카테고리(${cat}) 흔한 상황 예: ${situations}
   - 진짜 '감정'을 실는다: 후회·짜증·안도·뿌듯·민망·놀람 같은 사람 감정.
   - "어 이거 완전 내 얘기네"라는 공감이 나야 멈춘다. 남 얘기처럼 들리면 그냥 넘긴다.

④ 사람 말투 (AI 티 제거)
   - 실제 사람이 친구에게 툭 던지듯. 정형 카피·번역체·과장어 금지.
   - 다음 단어가 보이면 즉시 실패: ${blacklistSample} …

[절대 금지]
   - 정보나열형: "추천 BEST / 순위 / TOP / 모음 / 총정리 / 리스트 / 정리" → 공장 티. 금지.
   - 결론 노출: 제목에 답을 다 적기 → 갭 소멸.
   - 키워드 누락 또는 뒤배치 → 검색 사망.

[좋은 예 — 4가지 동시 충족]
   "${kw}, 장마철 옷장 곰팡이 보고 샀다가 전기세 보고 놀란 이유"
     → 키워드 앞 + 상황(곰팡이 옷장) + 감정(놀람) + 갭(왜?) + 사람 말투
   "${kw} 싼 거 샀다가 결국 두 번 산 후회"
     → 키워드 앞 + 감정(후회) + 갭(왜 두 번?) + 사람 말투

[나쁜 예]
   "${kw} 추천 BEST 5"        → 나열형 / 갭 없음 / 감정 없음 (공장 티)
   "충격! ${kw}의 놀라운 진실"  → AI 과장 / 낚시 / 사람 냄새 0

[발행 전 자가검토]
   □ 메인키워드 앞 1/3 배치?  □ 결론·숫자 감췄나?  □ 구체 상황 1개 이상?
   □ 진짜 감정 1개 이상?  □ 나열형/과장어 0개?  □ 친구에게 말하듯 자연스러운가?
   → 하나라도 "아니오"면 제목을 다시 쓴다.
════════════════════════════════════════════════════════════
`;
}
