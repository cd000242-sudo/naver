  generateParagraphStructure(totalParagraphs: number = 20): number[] {
    const distribution = {
      oneLine: Math.floor(totalParagraphs * 0.25),      // 한 줄 강조 (25%)
      shortParagraph: Math.floor(totalParagraphs * 0.35), // 2~3줄 (35%)
      mediumParagraph: Math.floor(totalParagraphs * 0.30), // 4~5줄 (30%)
      longParagraph: Math.floor(totalParagraphs * 0.10)   // 6줄+ (10%)
    };

    const paragraphLengths: number[] = [
      ...Array(distribution.oneLine).fill(1),
      ...Array(Math.floor(distribution.shortParagraph / 2)).fill(2),
      ...Array(Math.ceil(distribution.shortParagraph / 2)).fill(3),
      ...Array(Math.floor(distribution.mediumParagraph / 2)).fill(4),
      ...Array(Math.ceil(distribution.mediumParagraph / 2)).fill(5),
      ...Array(distribution.longParagraph).fill(6)
    ];

    return paragraphLengths.sort(() => Math.random() - 0.5);
  }

