/**
 * sync-build-define.mjs
 *
 * Reads version from package.json and writes src/runtime/version.generated.ts.
 * Idempotent: skips write if the version string is already up to date.
 *
 * Usage: node scripts/sync-build-define.mjs
 */

import { readFile, writeFile } from 'node:fs/promises';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

const PKG_PATH = resolve(ROOT, 'package.json');
const OUT_PATH = resolve(ROOT, 'src', 'runtime', 'version.generated.ts');

async function main() {
  // Read package.json
  let pkg;
  try {
    const raw = await readFile(PKG_PATH, 'utf8');
    pkg = JSON.parse(raw);
  } catch (err) {
    console.error(`[sync-build-define] ERROR: Cannot read package.json at ${PKG_PATH}`);
    console.error(`[sync-build-define] ${err.message}`);
    process.exit(1);
  }

  const version = pkg.version;
  if (!version) {
    console.error('[sync-build-define] ERROR: "version" field missing in package.json');
    process.exit(1);
  }

  const content =
    `// AUTO-GENERATED by scripts/sync-build-define.mjs. DO NOT EDIT.\n` +
    `export const APP_VERSION = '${version}';\n`;

  // Idempotent check: skip write if content is already identical
  let existing = '';
  try {
    existing = await readFile(OUT_PATH, 'utf8');
  } catch {
    // File does not exist yet — will be created below
  }

  if (existing === content) {
    console.log(`[sync-build-define] APP_VERSION=${version} (no change, skipping write)`);
    return;
  }

  await writeFile(OUT_PATH, content, 'utf8');
  console.log(`[sync-build-define] APP_VERSION=${version} -> ${OUT_PATH}`);
}

main();
